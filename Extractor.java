import java.io.*;
import java.util.*;

public class Extractor {
    public static void main(String[] args) throws Exception {
        Scanner key = new Scanner(System.in);

        System.out.print("Enter name of image file with hidden blocks: ");
        ImageReader extraction = new ImageReader("StegResults/" + key.next());

        List<Segment> HidedSegments = extraction.getHiderSegs();

        System.out.println("Converted to bytes: " + HidedSegments.size() * 64 / 8192 + "kB");

        CreateFile secret = new CreateFile(HidedSegments);
        secret.contructFile();
    }
}
